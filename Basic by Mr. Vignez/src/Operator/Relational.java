package Operator;

public class Relational {

	public static void main(String[] args) {
		int a =10;
		int b=20;
		System.out.println("Check for (A==B) " + (a==b));
		System.out.println("Check for (A!=B) " + (a!=b));
		System.out.println("Check for (A>B) " + (a>b));
		System.out.println("Check for (A<B) " + (a<b));
		System.out.println("Check for (A>=B) " + (a>=b));
		System.out.println("Check for (A<=B) " + (a<=b));
	}

}
